#include<iostream>
#include<math.h>
#include<stdlib.h>
using namespace std;
int main()
{
    cout<<"ans:"<<rand()%10<<endl;
    cout<<"ans:"<<pow(2,6)<<endl;
    cout<<"ans:"<<sqrt(16)<<endl;
    cout<<"ans:"<<sqrt(9)<<endl;
    cout<<"ans:"<<abs(16.8)<<endl;
    cout<<"ans:"<<abs(-8.6)<<endl;
    cout<<"ans:"<<round(16.8)<<endl;
    cout<<"ans:"<<abs(-3.4)<<endl;
    cout<<"ans:"<<round(-3.4)<<endl;
    cout<<"ans:"<<floor(-3.2)<<endl;
    cout<<"ans:"<<floor(-3.8)<<endl;
    cout<<"ans:"<<ceil(3.6)<<endl;
    cout<<"ans:"<<ceil(-3.8)<<endl;
}